// Experiment with the various ways to instantiate an array.
// In this exercise, you need to replace the '???' in the code
// by actual code.

// Create an array containing the integers 1, 2 and 3 using the 'new' keyword
val array1 = new Array[Int](3)
array1(0) = 1
array1(1) = 2
array1(2) = 3

// Create an array containing the integers 1, 2 and 3, without using 'new'
val array2 = Array(1, 2, 3)
